    def resourceLimitMax(self, state, alpha, beta, depth):
        """
        Implementation of the max function for minimax with resource limits (iterative deepening)
        Used with resourceLimitMin

        Args:
            state (2D): 2D list representation of the gameboard
            alpha (AIMove): the lower bound move to take
            beta (AIMove): the upper bound move to take
            depth (int): how far of depth should the algo ago

        Returns:
            AIMove: the desired move to take
        """
        validMoves = self.getMoves(state)
        if depth == 0 or not validMoves:
            return AIMove(score=self.evalUtilityState(state, 1))
        
        v = AIMove(score=-inf)
        for move in validMoves:
            state[ move[0] ][ move[1] ] = 1
            m = AIMove(move[0], move[1], self.resourceLimitMin(state, alpha, beta, depth - 1).score)
            state[ move[0] ][ move[1] ] = 0
            v = max(v, m)
            if v >= beta:
                return v
            alpha = max(v, alpha)
        return v

    def resourceLimitMin(self, state, alpha, beta, depth):
        """
        Implementation of the min function for minimax with resource limits (iterative deepening)
        Used with resourceLimitMin

        Args:
            state (2D): 2D list representation of the gameboard
            alpha (AIMove): the lower bound move to take
            beta (AIMove): the upper bound move to take
            depth (int): how far of depth should the algo ago

        Returns:
            AIMove: the desired move to take
        """
        validMoves = self.getMoves(state)
        if depth == 0 or not validMoves:
            return AIMove(score=self.evalUtilityState(state, 2))
    
        v = AIMove(score=inf)
        for move in validMoves:
            state[ move[0] ][ move[1] ] = 2
            m = AIMove(move[0], move[1], self.resourceLimitMax(state, alpha, beta, depth - 1).score)
            state[ move[0] ][ move[1] ] = 0
            v = min(v, m)
            if v <= alpha:
                return v
            beta = min(v, beta)
        return v 


    def getMoves(self, state):
        """
        Find the valid moves within the state passed into this function
        Returns a list representing legal moves 

        Args:
            state (list): 2D representation of the search tree

        Returns:
            list: 2D list representing the row and column that is a valid move within the current state
        """
        moves = []
        for column in range(self.__COL):
            if state[0][column] == 0 and state[1][column] != 0:
                moves.append([0, column])

            if state[1][column] == 0 and state[2][column] != 0:
                moves.append([1, column])

            if state[2][column] == 0 and state[3][column] != 0:
                moves.append([2, column])

            if state[3][column] == 0 and state[4][column] != 0:
                moves.append([3, column])

            if state[4][column] == 0 and state[5][column] != 0:
                moves.append([4, column])

            if state[5][column] == 0:
                moves.append([5, column])
        return moves

    def eval(self, state, player):
        """
        This function checks the desirability of the gameboard for horizontal, vertical, positive diagnol, and negative diagnol

        Args:
            state (list): 2D list representation of the gameboard
            player (int): int describing the player that is playing

        Returns:
            int: the desirability/score of the gamebaord
        """
        score = 0       

        # horizontal check
        for row in range(self.__ROW):
            rowArray = [int(i) for i in list(state[row, :])]
            for col in range(self.__COL - 3):
                window = rowArray[col : col + 4]
                score += self.evalUtilityState(window, player)
        
        # vertical check
        for col in range(self.__COL):
            colArray = [int(i) for i in list(state[ :, col])]
            for row in range(self.__ROW - 3):
                window = colArray[row : row + 4]
                score += self.evalUtilityState(window, player)
        
        # vertical diagnol check
        for row in range(self.__ROW - 3):
            for col in range(self.__COL - 3):
                window = [state[row + i][col + i] for i in range(4)]
                score += self.evalUtilityState(window, player)
        
        # negative diagnol check
        for row in range(self.__ROW - 3):
            for col in range(self.__COL - 3):
                window = [state[row + 3 - i][col + i] for i in range(4)]
                score += self.evalUtilityState(window, player)
        return score


    def evalUtilityState(self, window, player):
        """
        This evaluates the desirability of a given window
        For instance you would rather have a move with four of your pieces or three of yours

        Args:
            window (list): representing a window/range of the board
            player (int): 1 is for max and 2 is for min

        Returns:
            score: the desirability of this state
        """
        oppPlayer = 0
        score = 0

        if player == 1:
            oppPlayer = 2
        else:
            oppPlayer = 1 

        if window.count(player) == 4:
            score += 100
        elif window.count(player) == 3 and window.count(0) == 1:
            score += 5
        elif window.count(player) == 2 and window.count(0) == 2:
            score += 2
        if window.count(oppPlayer) == 3 and window.count(0) == 1:
            score -= 4
        return score


